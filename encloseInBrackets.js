/*
45. Enclose In Brackets

-Given a string, enclose it in round brackets.

EXAMPLE:
string = "abacaba", the output should be "(abacaba)".

*/

function encloseInBrackets(word){

    return "(" + word + ")";

}

console.log(encloseInBrackets("abacaba"));
console.log(encloseInBrackets("Nazareth"));
console.log(encloseInBrackets("JavaScript"));